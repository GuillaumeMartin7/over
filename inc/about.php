<h1 class="titlehone">ABOUT</h1>

<p class="about-introP">
    OVER est une agence étudiante couvrant principalement le web et la communication visuelle. <br>
    De l'élaboration d'identité graphique à l'integration de solutions technologiques pour vos besoins, nous conseillons et orientons
    des organismes, des associations, des entreprises et des particuliers.
    <br><br>
    Le monde numérique et connecté qui nous entoure fait qu'aujourd'hui, il est pratiquement indispensable d'être présent sur le Web
    pour construire et améliorer votre présence et votre notoriété.
    <br><br>
    Passionné avant tout, nous souhaitons partager nos ambitions de developpement afin d'exploiter de nouvelles idées avec des moyens
    les plus créatifs.
    Nous souhaitons inspirer le public tout en proposant une des meilleurs expériences utilisateurs pour répondre à vos principaux
    objectifs.
